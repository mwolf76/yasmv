/**
 * Runs the parser SMV rule on an input .smv file.
 *
 * @returns true if parsing was successful, false otherwise.
 */
bool parseFile(const char* fName)
{
    pANTLR3_INPUT_STREAM input;
    pANTLR3_COMMON_TOKEN_STREAM tstream;

    psmvParser psr;
    psmvLexer  lxr;

    DEBUG
        << "Parsing smv file "
        << fName
        << " ..."
        << std::endl;

    struct timespec start_clock;
    clock_gettime(CLOCK_MONOTONIC, &start_clock);

    input = antlr3FileStreamNew((pANTLR3_UINT8) fName, ANTLR3_ENC_8BIT);

    if (!input)
        throw FileInputException(fName);

    lxr = smvLexerNew(input); // smvLexerNew is generated by ANTLR
    assert(lxr);

    tstream = antlr3CommonTokenStreamSourceNew(ANTLR3_SIZE_HINT, TOKENSOURCE(lxr));
    assert(tstream);

    psr = smvParserNew(tstream);  // smvParserNew is generated by ANTLR3
    assert(psr);

    parseErrors = false;
    psr->pParser->rec->displayRecognitionError = yasmvdisplayRecognitionError;

    psr->smv(psr);

    // cleanup
    psr->free(psr);
    tstream->free(tstream);
    lxr->free(lxr);
    input->close(input);

    struct timespec stop_clock;
    clock_gettime(CLOCK_MONOTONIC, &stop_clock);

    reportParserStatus(parseErrors, start_clock, stop_clock);
    return ! parseErrors;
}

/**
 * Runs the parser COMMAND_LINE rule on an input line string.
 */
// FIXME: proper error handling
cmd::CommandVector_ptr parseCommand(const char *command_line)
{
    pANTLR3_INPUT_STREAM input;
    pANTLR3_COMMON_TOKEN_STREAM tstream;

    psmvParser psr;
    psmvLexer  lxr;

    DEBUG
        << "Parsing command ..."
        << std::endl;

    struct timespec start_clock;
    clock_gettime(CLOCK_MONOTONIC, &start_clock);

    // Preprocess command line to handle shell-style comments
    std::string processed_command;
    const char* p = command_line;
    bool in_string = false;
    char string_delimiter = '\0';
    
    // Process the command line, removing comments but preserving # inside strings
    while (*p) {
        if (!in_string) {
            // Check for string delimiters
            if (*p == '"' || *p == '\'') {
                in_string = true;
                string_delimiter = *p;
                processed_command += *p;
            }
            // Check for comment start
            else if (*p == '#') {
                // Skip the rest of the line
                break;
            }
            else {
                processed_command += *p;
            }
        }
        else {
            // Inside a string, copy everything
            processed_command += *p;
            // Check for end of string
            if (*p == string_delimiter) {
                in_string = false;
                string_delimiter = '\0';
            }
        }
        p++;
    }
    
    // Trim trailing whitespace
    while (!processed_command.empty() && 
           (processed_command.back() == ' ' || processed_command.back() == '\t')) {
        processed_command.pop_back();
    }
    
    // If the processed command is empty, return empty CommandVector
    if (processed_command.empty()) {
        return new cmd::CommandVector();
    }

    input = antlr3StringStreamNew((pANTLR3_UINT8) processed_command.c_str(),
                                  ANTLR3_ENC_8BIT, processed_command.length(), (pANTLR3_UINT8) "command");

    assert(input);

    lxr = smvLexerNew(input); // smvLexerNew is generated by ANTLR
    assert(lxr);

    tstream = antlr3CommonTokenStreamSourceNew(ANTLR3_SIZE_HINT, TOKENSOURCE(lxr));
    assert(tstream);

    psr = smvParserNew(tstream);  // smvParserNew is generated by ANTLR3
    assert(psr);

    parseErrors = false;
    psr->pParser->rec->displayRecognitionError = yasmvdisplayRecognitionError;

    cmd::CommandVector_ptr res
        (psr -> command_line(psr));

    // cleanup
    psr->free(psr);
    tstream->free(tstream);
    lxr->free(lxr);
    input->close(input);

    struct timespec stop_clock;
    clock_gettime(CLOCK_MONOTONIC, &stop_clock);

    reportParserStatus(parseErrors, start_clock, stop_clock);
    return ! parseErrors
        ? res : NULL;
}

